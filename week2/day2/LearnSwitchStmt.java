package week2.day2;

public class LearnSwitchStmt {
	public static void main(String[] args) {
		int value = 4;
		switch(value) {
		case 1 : System.out.println("Print Ticket for Balcony");
		break;
		case 2 : System.out.println("Print Ticket for first class");
		break;
		case 3 : System.out.println("Print Ticket for second class");
		break;
		case 4 : System.out.println("Print Ticket for third class");
		break;
		default : System.out.println("Please select 1,2,3,4");
		break;
		}
	} 
		
	}


